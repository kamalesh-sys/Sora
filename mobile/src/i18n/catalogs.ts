const englishIds = new Set([...Object.keys(ta), ...Object.keys(hi)]);
const en = Object.fromEntries(Array.from(englishIds, (message) => [message, message])) as Catalog;

export const catalogs: Record<AppLanguage, Catalog> = { en, ta, hi };

export function isAppLanguage(value: string | null | undefined): value is AppLanguage {
  return supportedLanguages.includes(value as AppLanguage);
}

export function resolveAppLanguage(value?: string | null): AppLanguage {
  const languageCode = (value ?? "").trim().toLowerCase().split(/[-_]/)[0];
  return isAppLanguage(languageCode) ? languageCode : "en";
}

export function getDeviceLanguage(): AppLanguage {
  try {
    const locale = getLocales()[0];
    return resolveAppLanguage(locale?.languageCode ?? locale?.languageTag);
  } catch {
    return "en";
  }
}

let activeLanguage: AppLanguage = getDeviceLanguage();

export function setActiveLanguage(language: AppLanguage) {
  activeLanguage = language;
}

export function getActiveLanguage() {
  return activeLanguage;
}

export function getLocaleTag(language: AppLanguage = activeLanguage) {
  return localeTags[language];
}

export function translate(language: AppLanguage, message: string, values?: TranslationValues) {
  const template = catalogs[language][message] ?? catalogs.en[message] ?? message;
  if (!values) return template;

  return template.replace(/\{([a-zA-Z0-9_]+)\}/g, (match, key: string) => {
    const value = values[key];
    return value === null || value === undefined ? match : String(value);
  });
}

export function createTranslator(language: AppLanguage): Translate {
  return (message, values) => translate(language, message, values);
}

export function translateActive(message: string, values?: TranslationValues) {
  return translate(activeLanguage, message, values);
}
